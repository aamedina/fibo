(defn emit-ns
  "Emits a model to a Clojure namespace using metadata map."
  [model md arg-map]
  (if-some [prefix (or (:prefix arg-map)
                       (:rdfa/prefix md)
                       (:vann/preferredNamespacePrefix md))]
    (let [ns-file (str (or (:target arg-map) rdf/*target*)
                       (namespace-munge (str/replace prefix #"\." "/"))
                       ".cljc")]
      (doseq [form model]
        (spit ns-file
              (binding [*print-namespace-maps* nil
                        *print-meta*           true]
                (with-out-str
                  (zprint/zprint (list 'def (symbol (name (:db/ident form))) form)
                                 {:map    {:justify?      true
                                           :nl-separator? false
                                           :hang?         true
                                           :indent        0
                                           :sort-in-code? true
                                           :force-nl?     true}
                                  :vector {:wrap? false}})
                  (newline)))
              :append true)))
    (throw (ex-info "must provide prefix in metadata map" {:md md :arg-map arg-map}))))

