(declare quickstart)

(defrecord FIBO [vocab domains modules submodules]
  com/Lifecycle
  (start [this]
    (try
      (binding [*ns* (find-ns (or (:quickstart-ns this) 'dev))]
        (quickstart))
      (rdf/import-from 'net.wikipunk.rdf.fibo-fbc-fct-mkti
                       'net.wikipunk.rdf.gleif-L1-data)
      (catch Throwable ex))
    this)
  (stop [this]
    this)

  rdf/NamespaceSpitter
  (emit [_ arg-map]
    (rdf/emit domains arg-map)
    (rdf/emit modules arg-map)
    (rdf/emit submodules arg-map)))

